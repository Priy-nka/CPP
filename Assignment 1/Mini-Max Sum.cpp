void miniMaxSum(vector<int> arr) {
    long max=0,min=0;
    sort(arr.begin(),arr.end());
    for (int i=0;i<4;i++)
    {
        min+=arr[i];
    }
    for (int j=1;j<5;j++)
    {
        max+=arr[j];
    }
    
    cout<<min<<" "<<max;
}

